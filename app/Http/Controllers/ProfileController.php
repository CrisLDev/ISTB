    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {

        $id = auth()->user()->id;

        $userInfo = User::where('id', $id)->first();

        $profileInfo = Profile::where('user_id', $id)->first();

